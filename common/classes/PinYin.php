class PinYin {

	/**
	 * 获取汉字拼音
	 * 
	 * @param $search string 转换中文
	 * @param $flag int
	 */
	public static function getPinYin($search, $flag = 0) {
		$ret = "";
		$str = iconv( "utf-8", "gbk", $search );
		$str_len = strlen( $str );
		for($i = 0; $i < $str_len; $i ++) {
			$p = substr( $str, $i, 1 );
			if ((ord( $p ) >= 129) && (ord( $p ) <= 254)) { // GBK范围：首字节81~FE,尾字节40~FE
				$q = substr( $str, $i + 1, 1 );
				if ((ord( $q ) >= 64) && (ord( $q ) <= 254)) { // GBK范围：首字节81~FE,尾字节40~FE
					$p = substr( $str, $i, 2 );
					++ $i;
					$q = iconv( "gbk", "utf-8", $p );
				}
				$j = 1;
				foreach ( self::$code_data as $pinyin => $word ) {
					if (preg_match( "/$q/", $word ) == 1) {
						if ($i == 1 && $ret == "") { // 汉字情况起始情况，开始+1，排在数字字母后面
							$ret = '1';
						}
						$wpy = $pinyin;
						$j ++;
						break;
					}
				}
				if ($j == 1) {
					$wpy = $q;
				}
			} else {
				if ($i == 0) { // 非汉字情况起始情况，开始+0，确保靠前排序
					$ret = '0';
				}
				$wpy = $p;
			}
			$ret .= $wpy;
		}
		if ($ret == "") { // 非GBK字符起始情况，不考虑，用默认的mysql排序
			if ($flag) {
				$ret = '3' . $search;
			} else {
				$ret = $search;
			}
		}
		return $ret;
	}


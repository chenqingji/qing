 * 获取全部拼音
 * $search: 待转换的字符串
 * 规则：	可识别的gbk非汉字开头 ==> 0+转换后的串
 * 可识别的gbk汉字 ==> 1+转换后的串
 * 转gbk，不可识别的串 ==> 直接赋值原串，不做处理
 * 返回： $search的汉字转换为拼音串
